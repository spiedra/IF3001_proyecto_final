CONSTRAINT PK_ESTUDIANTE_BECA PRIMARY KEY (ID_ESTUDIANTE, ID_CURSO)
,CONSTRAINT FK_ID_ESTUDIANTE_D FOREIGN KEY (ID_ESTUDIANTE) REFERENCES tb_ESTUDIANTE (ID_ESTUDIANTE)
,CONSTRAINT FK_ID_CURSO_E FOREIGN KEY (ID_CURSO) REFERENCES tb_CURSO (ID_CURSO)
